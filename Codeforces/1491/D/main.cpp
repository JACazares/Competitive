int Q;

int main()
{
	optimizeIO();
	cin >> Q;
	while(Q--)
	{
		int u, v;
		cin >> u >> v;
		if(u <= v && pct(u) >= pct(v))
		{
			vi a1, a2;
			for(int i = 0; i < 30; i++)
			{
				if(sz(a1) == pct(v))
					break;
				if(u & (1 << i))
					a1.pb(i);
			}
			for(int i = 0; i < 30; i++)
				if(v & (1 << i))
					a2.pb(i);
			int ans = 1;
			for(int i = 0; i < pct(v); i++)
				if(a1[i] > a2[i])
					ans = 0;
			
			if(ans)
				cout << "YES\n";
			else
				cout << "NO\n";
		}
		else
			cout << "NO\n";
	}
	return 0;
}
